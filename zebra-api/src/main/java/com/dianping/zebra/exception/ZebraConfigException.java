package com.dianping.zebra.exception;

public class ZebraConfigException extends ZebraException {

	private static final long serialVersionUID = -7049590514431591540L;

	public ZebraConfigException() {
		super();
	}

	public ZebraConfigException(String message) {
		super(message);
	}

	public ZebraConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public ZebraConfigException(Throwable cause) {
		super(cause);
	}
}
